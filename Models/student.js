const mongoose = require("mongoose");

const studentSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    classrooms: {
        type: [String]
    }
});

module.exports = new mongoose.model("Student", studentSchema);